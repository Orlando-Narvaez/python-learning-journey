print("*** Operadores de Asignacion Compuestos ***")
a,b = 10, 5
print(f"Valores iniciales: a={a}, b={b}")

# Operador compuesto de una suma +=
a += b
print(f"Después de a += b: a={a}")

# Operador compuesto de una resta -=
a -= b
print(f"Después de a -= b: a={a}")

# Operador compuesto de una multiplicacion *=
a *= b
print(f"Después de a *= b: a={a}")

# Operador compuesto de una division /=
a /= b
print(f"Después de a /= b: a={a}")

# Operador compuesto de una division entera //=
a //= b
print(f"Después de a //= b: a={a}")

# Operador compuesto de un modulo %=
a %= b
print(f"Después de a %= b: a={a}")

# Operador compuesto de un exponente **=
a **= b
print(f"Después de a **= b: a={a}")