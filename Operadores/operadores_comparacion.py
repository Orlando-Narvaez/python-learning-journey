print("*** Operadores de comparacion (relacionales) ***")
a, b = 10, 5
print(f"Valores: a={a}, b={b}")

# Operador de igualdad ==
resultado = (a == b)
print(f"resultado de a == b: {resultado}")

# Operador de diferencia !=
resultado = (a != b)
print(f"resultado de a != b: {resultado}")

# Operador mayor que >
resultado = (a > b)
print(f"resultado de a > b: {resultado}")

# Operador menor que <
resultado = (a < b)
print(f"resultado de a < b: {resultado}")

# Operador mayor o igual que >=
resultado = (a >= b)
print(f"resultado de a >= b: {resultado}")

# Operador menor o igual que <=
resultado = (a <= b)
print(f"resultado de a <= b: {resultado}")