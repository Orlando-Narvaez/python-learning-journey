# Operadores Aritmeticos

a = 10
b = 3

# Suma +
suma = a + b
print(f"Suma: {suma}")

# Resta -
resta = a - b
print(f"Resta: {resta}")

# Multiplicacion *
multiplicacion = a * b
print(f"Multiplicacion: {multiplicacion}")

# Division /
division = a / b
print(f"Division: {division:.2f}")

# Division Entera //
division_entera = a // b
print(f"Division Entera: {division_entera}")

# Modulo %
modulo = a % b
print(f"Modulo (residuo de la division): {modulo}")

# Exponente **
exponente = a ** b
print(f"Exponente: {exponente}")