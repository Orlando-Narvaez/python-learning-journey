# Manejo de subcadenas
cadena = 'Hola, mundo!'

# Obtenemos la subcadena de "Hola"
subcadena_hola = cadena[0:4] # desde el indice 0 hasta el 4 (no incluye el 4)
print(f'Subcadena de "Hola": {subcadena_hola}')

# Obtenemos la subcadena de "mundo"
subcadena_mundo = cadena[6:11] # desde el indice 6 hasta el 11 (no incluye el 11)
print(f'Subcadena de "mundo": {subcadena_mundo}')