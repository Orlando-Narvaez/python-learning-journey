# Ejemplo de tipo de datos en Python

# Entero
edad = 25
print("Edad:", edad, "Tipo:", type(edad))

# Flotante
altura = 1.71
print("Altura:", altura, "Tipo:", type(altura))

# Cadena de texto
nombre = "Orlando"
print("Nombre:", nombre, "Tipo:", type(nombre)) 

# Booleano
es_estudiante = True
print("Es estudiante:", es_estudiante, "Tipo:", type(es_estudiante))

# None, usencia de valor
direccion = None
print("Direcci√≥n:", direccion, "Tipo:", type(direccion))
