print("*** Ciclo For ***")

print("Imprimimos los valores de una cadena de texto")
cadena = "Hola Mundo"
# Iteramos los caracteres
for letra in cadena:
    print(letra, end=" ")   
    
print("\n")
    
print("Imprimimos los valores de una lista")
frutas =["Manzana", "Pera", "Mango"]
for fruta in frutas:
    print(fruta, end=" ")